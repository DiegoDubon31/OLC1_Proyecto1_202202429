package Analyzer;

/**
 *
 * @author diego
 */
public class Token {
    private String lexeme;
    private String row;
    private String column;
    private String type;

    public Token(String lexeme, String row, String column, String type) {
        this.lexeme = lexeme;
        this.row = row;
        this.column = column;
        this.type = type;
    }

    public String getLexeme() {
        return lexeme;
    }

    public void setLexeme(String lexeme) {
        this.lexeme = lexeme;
    }

    public String getRow() {
        return row;
    }

    public void setRow(String row) {
        this.row = row;
    }

    public String getColumn() {
        return column;
    }

    public void setColumn(String column) {
        this.column = column;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    
    
}
